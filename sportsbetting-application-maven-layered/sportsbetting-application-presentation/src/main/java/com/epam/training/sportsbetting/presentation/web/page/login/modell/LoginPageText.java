package com.epam.training.sportsbetting.presentation.web.page.login.modell;

/** Provides texts to the login page.
 * */
public class LoginPageText {

    private String title;
    private String description;
    private String login;
    private String orText;
    private String register;
    private String toStart;
    private String loginButton;
    private String passwordField;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getRegister() {
        return register;
    }

    public void setRegister(String register) {
        this.register = register;
    }

    public String getToStart() {
        return toStart;
    }

    public void setToStart(String toStart) {
        this.toStart = toStart;
    }

    public String getLoginButton() {
        return loginButton;
    }

    public void setLoginButton(String loginButton) {
        this.loginButton = loginButton;
    }

    public String getPasswordField() {
        return passwordField;
    }

    public void setPasswordField(String passwordField) {
        this.passwordField = passwordField;
    }

    public String getOrText() {
        return orText;
    }

    public void setOrText(String orText) {
        this.orText = orText;
    }

}
