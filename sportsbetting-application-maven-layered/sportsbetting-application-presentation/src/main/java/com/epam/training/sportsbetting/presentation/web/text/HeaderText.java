package com.epam.training.sportsbetting.presentation.web.text;

/** Provides texts to the header jsp.
 * */
public class HeaderText {
    private String home;
    private String events;
    private String language;
    private String logout;
    private String hunUrl;
    private String engUrl;

    public String getHome() {
        return home;
    }

    public void setHome(String home) {
        this.home = home;
    }

    public String getEvents() {
        return events;
    }

    public void setEvents(String events) {
        this.events = events;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getLogout() {
        return logout;
    }

    public void setLogout(String logout) {
        this.logout = logout;
    }

    public String getHunUrl() {
        return hunUrl;
    }

    public void setHunUrl(String hunUrl) {
        this.hunUrl = hunUrl;
    }

    public String getEngUrl() {
        return engUrl;
    }

    public void setEngUrl(String engUrl) {
        this.engUrl = engUrl;
    }

}
